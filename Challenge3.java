public class Challenge3 {
    public static void main(String[] args) {

        // Use variables to calculate the area and perimeter of a 
        // rectangle with a width of 5.0 and a height of 10.0. Print out the area and perimeter.
       
        double width = 5.0;
        double height = 10.0;
 
        System.out.println(width * height);
        System.out.println("This is your area.");
        System.out.println((2) * (width + height));
        System.out.println("This is your perimeter.");
    }
}
